/**
 * @author kojin15.
 */
@Mod(modid = MOD_ID, name = MOD_NAME, version = MOD_VERSION, acceptedMinecraftVersions = "[1.12]",
        modLanguage = "kotlin", modLanguageAdapter = "com.kojin15.workbenchplus.KotlinAdapter",
        useMetadata = true)
object WorkbenchPlus {
    const val MOD_ID = "workbenchplus"
    const val MOD_NAME = "Workbench Plus"
    const val MOD_VERSION = "1.0.0"

    @Mod.Metadata
    private lateinit var METADATA: ModMetadata

    @SidedProxy(clientSide = "com.kojin15.workbenchplus.proxy.ClientProxy",
            serverSide = "com.kojin15.workbenchplus.proxy.ServerProxy")
    lateinit var PROXY: CommonProxy

    @Mod.EventHandler
    fun construct(event: FMLConstructionEvent) {
        METADATA.modId = MOD_ID
        METADATA.name = MOD_NAME
        METADATA.version = MOD_VERSION
        METADATA.authorList.add("kojin15")
        METADATA.description = ""
        METADATA.autogenerated = false
    }

    @Mod.EventHandler
    fun preInit(event: FMLPreInitializationEvent) {
        PROXY.preInit()
    }

    @Mod.EventHandler
    fun init(event: FMLInitializationEvent) {
        PROXY.init()
    }

    @Mod.EventHandler
    fun postInit(event: FMLPostInitializationEvent) {
        PROXY.postInit()
    }

    object Blocks {
        @JvmField val workbenchplus = BlockWorkbenchPlus().setRegistryName(MOD_ID, "workbenchplus")
    }
}

object WPGuiHandler : IGuiHandler {
    val GUI_ID_WORKBENCHPLUS = 1

    override fun getClientGuiElement(ID: Int, player: EntityPlayer, world: World, x: Int, y: Int, z: Int): Any? {
        return when (ID) {
            GUI_ID_WORKBENCHPLUS -> GuiWorkbenchPlus(world.getTileEntity(BlockPos(x, y, z)) as TileWorkbenchPlus, player)
            else -> null
        }
    }

    override fun getServerGuiElement(ID: Int, player: EntityPlayer, world: World, x: Int, y: Int, z: Int): Any? {
        return when (ID) {
            GUI_ID_WORKBENCHPLUS -> ContainerWorkbenchPlus(world.getTileEntity(BlockPos(x, y, z)) as TileWorkbenchPlus, player)
            else -> null
        }
    }
}

object Translator {
    fun translateToLocal(key: String): String = TextComponentTranslation(key).formattedText
}